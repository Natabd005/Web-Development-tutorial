let i = 0;

//WHILE LOOP
while(i<5){
    console.log('The number is '+i);
    i++;
}

//DO-WHILE LOOP
do{
    console.log('The number is '+i);
    i++;
}
while(i<5);


//FOR LOOP
for(i=0;i<5;i++){
    console.log('The number is '+i);
}

//EXAMPLE
for(i=0;i<50;i++){
    console.log(i+' Something');
}